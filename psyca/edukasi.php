<p class="judul_artikel">Apa itu PSYCA?
<p>Di Indonesia , penderita gangguan kejiwaan seringkali digeneralisirkan menjadi “tidak waras” atau bahkan “gila”.  Data statistik nasional juga mencatat, pada tahun 2014 kurang lebih 18.000 jiwa dipasung keluarganya sendiri karena kurangnya informasi dan edukasi mengenai penyakit mental. Padahal menurut ilmu kejiwaan, penderita penyakit mental belum tentu sama dengan orang tidak waras. Kurangnya edukasi turut mempengaruhi kesembuhan pasien apabila keluarga tidak mengetahui penanganan tepat terhadap penderita. Selain faktor-faktor tersebut, terdapat fakta lain bahwa banyak penderita gangguan mental yang belum mendapatkan bantuan dari psikolog, dikarenakan jumlah psikolog yang terbatas maupun keterbatasan ekonomi.
<p>PSYCA merupakan program Psychological Care yang diharapkan dapat memberikan informasi dan bantuan kepada masyarakat, terutama penderita gangguan dan orang-orang terdekatnya.
</p>

